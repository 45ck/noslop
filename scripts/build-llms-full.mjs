#!/usr/bin/env node

/**
 * Concatenates all documentation into llms-full.txt for full-context AI loading.
 * Run: node scripts/build-llms-full.mjs
 */

import { readFile, writeFile, readdir } from 'node:fs/promises';
import { join, dirname, posix } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');

const DOCS = [
  'README.md',
  'docs/guides/coding-agents.md',
  'docs/glossary.md',
  'docs/guides/greenfield.md',
  'docs/guides/monorepos.md',
  'docs/languages/typescript.md',
  'docs/languages/javascript.md',
  'docs/languages/rust.md',
  'docs/languages/dotnet.md',
  'docs/languages/python.md',
  'docs/languages/go.md',
  'docs/languages/java.md',
  'docs/languages/ruby.md',
  'docs/languages/kotlin.md',
  'docs/languages/swift.md',
  'docs/languages/php.md',
  'docs/languages/scala.md',
  'docs/languages/elixir.md',
  'docs/languages/dart.md',
  'docs/languages/haskell.md',
  'docs/languages/lua.md',
  'docs/languages/cpp.md',
  'docs/languages/zig.md',
  'docs/languages/ocaml.md',
];

// D3: Check that every .md file on disk is listed in DOCS
async function listMdFiles(dir, prefix) {
  const entries = await readdir(join(ROOT, dir));
  return entries.filter((f) => f.endsWith('.md')).map((f) => posix.join(prefix, f));
}

const onDisk = [
  ...(await listMdFiles('docs/languages', 'docs/languages')),
  ...(await listMdFiles('docs/guides', 'docs/guides')),
];

const missing = onDisk.filter((f) => !DOCS.includes(f));
if (missing.length > 0) {
  console.error(`ERROR: Doc files on disk not listed in DOCS array:\n  ${missing.join('\n  ')}`);
  console.error('Add them to the DOCS array in scripts/build-llms-full.mjs');
  process.exit(1);
}

const sections = [];

for (const rel of DOCS) {
  const abs = join(ROOT, rel);
  const content = await readFile(abs, 'utf8');
  sections.push(`# Source: ${rel}\n\n${content.trim()}`);
}

const output = `# noslop — complete documentation

> This file is auto-generated by scripts/build-llms-full.mjs.
> It concatenates all project documentation into a single file for AI context loading.

${sections.join('\n\n---\n\n')}
`;

const verifyMode = process.argv.includes('--verify');

if (verifyMode) {
  // Print to stdout for freshness comparison — no file write
  process.stdout.write(output);
} else {
  await writeFile(join(ROOT, 'llms-full.txt'), output, 'utf8');
  console.log(`llms-full.txt written (${DOCS.length} documents)`);
}
